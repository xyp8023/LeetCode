f000 = 2
f001 = 1
f010 = 3
f011 = -2
f100 = 2
f101 = -2
f110 = -4
f111 = 1

x = (f001+f010-f000-f011)/(f001+f010+f100+f111-f000-f011-f101-f110)
y = (f001+f100-f000-f101)/(f001+f010+f100+f111-f000-f011-f101-f110)
z = (f010+f100-f000-f110)/(f001+f010+f100+f111-f000-f011-f101-f110)

fxyz = (1-x)*(1-y)*(1-z)*f000+(1-x)*(1-y)*z*f001+\
       (1-x)*y*(1-z)*f010+(1-x)*y*z*f011+\
    x*(1-y)*(1-z)*f100+x*(1-y)*z*f101+\
    x*y*(1-z)*f110+x*y*z*f111

print(fxyz)
